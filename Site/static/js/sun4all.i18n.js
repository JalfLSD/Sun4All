function getDataTakenInfo(date) {
	if (language == 'pt-BR') {
		return 'Esta imagem foi obtida no dia ' + date.getDate() + ' de ' + monthNames_pt[date.getMonth()] + ' de ' + date.getFullYear();		
	} else {
		return 'This is an image taken in ' + monthNames_en[date.getMonth()] + ', ' + date.getDate() + ' - ' + date.getFullYear();
	}
}

